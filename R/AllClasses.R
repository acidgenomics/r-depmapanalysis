#' Gene effect in cancer cell lines
#'
#' @details
#' Inherits from `SummarizedExperiment`.
#' Cells in columns, genes in rows.
#'
#' @export
#' @note Updated 2022-08-19.
#'
#' @return `GeneEffect`.
#'
#' @seealso
#' - https://depmap.org/portal/achilles/
#' - https://depmap.org/ceres/
#' - https://score.depmap.sanger.ac.uk/
setClass(
    Class = "GeneEffect",
    contains = "SummarizedExperiment"
)
setValidity(
    Class = "GeneEffect",
    method = function(object) {
        ok <- validate(
            hasRownames(object),
            allAreMatchingRegex(
                x = rownames(object),
                pattern = "^[_A-Za-z0-9]+_[0-9]+$"
            ),
            hasColnames(object),
            allAreMatchingRegex(
                x = colnames(object),
                pattern = "^ACH_[0-9]{6}$"
            ),
            isSubset("effect", assayNames(object))
        )
        if (!isTRUE(ok)) {
            return(ok)
        }
        ok <- validateClasses(
            object = metadata(object),
            expected = append(
                x = .expectedSEMetadata,
                values = list(
                    "libraryType" = "character",
                    "scoringMethod" = "character"
                )
            ),
            subset = TRUE
        )
        if (!isTRUE(ok)) {
            return(ok)
        }
        switch(
            EXPR = metadata(object)[["libraryType"]],
            "crispr" = {
                ok <- validate(
                    isSubset("probability", assayNames(object)),
                    isSubset(
                        x = .expectedColData,
                        y = colnames(colData(object))
                    )
                )
                if (!isTRUE(ok)) {
                    return(ok)
                }
                ok <- validateClasses(
                    object = metadata(object),
                    expected = list(
                        "commonEssentials" = "character",
                        "controlCommonEssentials" = "character",
                        "controlNonessentials" = "character"
                    ),
                    subset = TRUE
                )
                if (!isTRUE(ok)) {
                    return(ok)
                }
            },
            "rnai" = {
                ok <- validate(
                    isSubset(
                        x = .expectedColData,
                        y = colnames(colData(object))
                    ),
                    isSubset(
                        x = c(
                            "inAchilles",
                            "inDrive",
                            "inMarcotte",
                            "marcotteName",
                            "marcotteSubtypeIntrinsic",
                            "marcotteSubtypeNeve",
                            "marcotteSubtypeThreeReceptor",
                            "novartisName",
                            "novartisPathologistAnnotation",
                            "novartisPrimarySite"
                        ),
                        y = colnames(colData(object))
                    )
                )
                if (!isTRUE(ok)) {
                    return(ok)
                }
            }
        )
        TRUE
    }
)
